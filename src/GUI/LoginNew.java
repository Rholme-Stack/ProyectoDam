    private void validarUsuario() {
        
        int resultado=0;  
      String usuario = jTextFieldUsuario.getText();
      String clave =String.valueOf(jPasswordFieldClave.getPassword());
      String SQL="Select * from usuarios where nombre='"+usuario+"' and clave='"+clave+"'";
      
        try {
            Statement st= con.createStatement();
            ResultSet rs=st.executeQuery(SQL);
            
            if(rs.next()){
                resultado=1;
                if(resultado==1){
                
                    //JOptionPane.showMessageDialog(null, "correto");
                    VentanaPrincipal miventana = new VentanaPrincipal ();
                    
                    miventana.setVisible(true);
                    miventana.setLocationRelativeTo(this);
                    miventana.setExtendedState(SistemaVentana.MAXIMIZED_BOTH);
                    Usuarios user = new Usuarios(jTextFieldUsuario.getText().toUpperCase());
                    String nombre = user.getNombre();
                    VentanaPrincipal.jLabelUsuario.setText(nombre);
                            
                    dispose();
                    
                }
                    
                
            
            }else{
            JOptionPane.showMessageDialog(this, "Error de acceso, usuario o contrase√±a incorrecto");
            }
                
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error Login" + e.getMessage());
        }
        
        
    }
